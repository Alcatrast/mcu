/**
 * Простой пример мигания светодиодом на Raspberry Pi Pico (RP2040)
 * Светодиод подключен к GPIO 25 (встроенный светодиод на плате)
 */

#include "pico/stdlib.h"
#include "hardware/gpio.h"

// Константа с номером GPIO вывода, к которому подключен светодиод
const uint LED_PIN = 25;

int main()
{
    // Инициализация всех стандартных функций Pico SDK (необязательно, но полезно)
    stdio_init_all();

    // Инициализация нужного нам GPIO вывода
    gpio_init(LED_PIN);
    
    // Настройка GPIO вывода как выход
    gpio_set_dir(LED_PIN, GPIO_OUT);

    // Бесконечный цикл — программа остаётся в нём, пока есть питание
    while (1)
    {
        // Включаем светодиод (подаём высокий уровень на вывод)
        gpio_put(LED_PIN, 1);
        // Ждём 250 миллисекунд
        sleep_ms(250);
        
        // Выключаем светодиод (низкий уровень)
        gpio_put(LED_PIN, 0);
        // Ждём 1000 миллисекунд (1 секунда)
        sleep_ms(1000);
    }
}